 let grade = prompt("Enter a letter grade as a capital letter.");
switch (grade){
case 'A':
  alert("Good job");
  break;
case 'B':
  alert("Pretty Good");
  break;
case 'C':
  alert("Passed");
  break;
case 'D':
  alert("not so good");
  break;
case 'F':
  alert("oops, failed");
  break;
default:
  alert("uh oh");
}